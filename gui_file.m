% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
clc

x0 = str2num(get(handles.x0,'String'));
x1 = str2num(get(handles.x1,'String'));
x2 = str2num(get(handles.x2,'String'));
x3 = str2num(get(handles.x3,'String'));
x4 = str2num(get(handles.x4,'String'));

y0 = str2num(get(handles.y0,'String'));
y1 = str2num(get(handles.y1,'String'));
y2 = str2num(get(handles.y2,'String'));
y3 = str2num(get(handles.y3,'String'));
y4 = str2num(get(handles.y4,'String'));

cell_k = (y1-y0)/(x1-x0);
cell_l = (y2-y1)/(x2-x1);
cell_m = (y3-y2)/(x3-x2);
cell_n = (y4-y3)/(x4-x3);
cell_o = (cell_l-cell_k)/(x2-x0);
cell_p = (cell_m-cell_l)/(x3-x1);
cell_q = (cell_n-cell_m)/(x4-x2);
cell_r = (cell_p-cell_o)/(x3-x0);
cell_s = (cell_q-cell_p)/(x4-x1);
cell_t = (cell_s-cell_r)/(x4-x0);

set(handles.cell_a,'String',num2str(x0));
set(handles.cell_b,'String',num2str(x1));
set(handles.cell_c,'String',num2str(x2));
set(handles.cell_d,'String',num2str(x3));
set(handles.cell_e,'String',num2str(x4));

set(handles.cell_f,'String',num2str(y0));
set(handles.cell_g,'String',num2str(y1));
set(handles.cell_h,'String',num2str(y2));
set(handles.cell_i,'String',num2str(y3));
set(handles.cell_j,'String',num2str(y4));

set(handles.cell_k,'String',num2str(cell_k));
set(handles.cell_l,'String',num2str(cell_l));
set(handles.cell_m,'String',num2str(cell_m));
set(handles.cell_n,'String',num2str(cell_n));
set(handles.cell_o,'String',num2str(cell_o));
set(handles.cell_p,'String',num2str(cell_p));
set(handles.cell_q,'String',num2str(cell_q));
set(handles.cell_r,'String',num2str(cell_r));
set(handles.cell_s,'String',num2str(cell_s));
set(handles.cell_t,'String',num2str(cell_t));

syms X
poly = y0 + cell_k * (X - x0) + cell_o * (X-x0)*(X-x1);
poly = poly + cell_r * (X-x0)*(X-x1)*(X-x2);
poly = poly + cell_t *(X-x0)*(X-x1)*(X-x2)*(X-x3);

set(handles.output_equation_1,'String',string(poly));

poly = simplify(poly);

set(handles.output_equation_2,'String',string(poly));

x_point = str2num(get(handles.x_point,'String'));
y_point = simplify(subs(poly,X,x_point));

y_point = sym2poly(y_point)

set(handles.y_point,'String',string(y_point));


